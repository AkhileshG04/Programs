package Basics;

public class OperatorExample {

	public static void main(String[] args) {
		System.out.println(10<<2);
		System.out.println(10<<3);
		System.out.println(20<<2);
		System.out.println(15<<2);
		
		System.out.println(10>>2);
		System.out.println(10>>2);
		System.out.println(10>>2);
		
		int a=2;
		int b=5;
		int min=(a<b)?a:b;
		System.out.println(min);	
		

	}

}
